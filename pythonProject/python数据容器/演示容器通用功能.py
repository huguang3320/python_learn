my_list = [1, 2, 3, 4, 5]
my_tuple = (1, 2, 3, 4, 5)
my_str = "abcdefg"
my_set = {1, 2, 3, 4, 5}
my_dict = {"key1": 1, "key2": 2, "key3": 3, "key4": 4, "key5": 5}

# len 求容器个数

# max 求容器最大值
print(f"列表最大值：{max(my_list)}")
print(f"元组最大值：{max(my_tuple)}")
print(f"字符串最大值：{max(my_str)}")
print(f"set集合最大值：{max(my_set)}")
print(f"字典最大值：{max(my_dict)}")
# min 求容器最小值

print(f"list最小值：{min(my_list)}")
print(f"元组最小值：{min(my_tuple)}")
print(f"字符串最小值：{min(my_str)}")
print(f"set集合最小值：{min(my_set)}")
print(f"字典最小值：{min(my_dict)}")

