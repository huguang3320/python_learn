# 将数字类型转化为字符串
num_str = str(1111)
print(type(num_str),num_str)
float_str = str(11.123)
print(type(float_str),float_str)

# 将字符串转换成数字类型
num1 = int("11")
print(type(num1),num1)
num2 = float("111.3123")
print(type(num2),num2)

# 数字类型转化成浮点类型
num3 = float(111)
print(type(num3),num3)

# 浮点类型转化成整数
num4 = int(11.123)
print(type(num4),num4)